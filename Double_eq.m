function [xsi1, xsi2]=Double_eq(n1, n2, T1)


K1 = 10 .^(-11650 ./ T1 + 13.076);
K2 = 10 .^(1910 ./ T1 - 1.764);
P = 26;


options = optimset('Display','off');


fct = @(V)[(3*V(1)+V(2)).^3 .* (V(1)-V(2)) .* P.^2./((n1-V(1)).*(n2-V(1)-V(2)).*(n1+n2+V(1).*2).^2) - K1;V(2) .* (3*V(1) + V(2))/((V(1)-V(2)).*(n2-V(1)-V(2))) - K2];

A = @(x,y)[(3*x+y).^3 .* (x-y) .* P.^2./((n1-x).*(n2-x-y).*(n1+n2+x.*2).^2) - K1];

B = @(x,y)[y .* (3*x + y)/((x-y).*(n2-x-y)) - K2];

Ax = @(x,y)[9.*(3*x+y).^2.*(x-y)*P^2 + (3*x+y).^3 .*P.^2 + K1*(n1 + n2 +2*x).^2.*(n1-x)-K1*(n1-x-y).*4.*(n1+n2+2*x).*(n1-x) + K1*(n1-x-y).*(n1+n2+2*x).^2];

Ay = @(x,y)[3.*(3*x+y).^2.*(x-y)*P^2 - (3*x+y).^3 .*P.^2 + K1*(n1-x)*(n1 + n2 +2*x).^2];

Bx = @(x,y)[3*y-K2*(n2-x-y) + K2*(x-y)];

By = @(x,y)[3*x+2*y+K2*(n2-x-y) + K2*(x-y)];

sol = fsolve(fct,[50,30],options);
xsi1 = sol(1);
xsi2 = sol(2);


end
